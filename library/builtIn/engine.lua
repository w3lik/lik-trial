local cc = { c = {}, i = {} }

--- 字符ID转整型ID
---@type fun(idChar:string):number
J.C2I = function(idChar)
    if (idChar == nil or type(idChar) ~= "string") then
        stack(idChar)
        return
    end
    local id = cc.c[idChar]
    if (id == nil) then
        id = ('>I4'):unpack(idChar)
        cc.c[idChar] = id
        cc.i[id] = idChar
    end
    return id
end

--- 整型ID转字符ID
---@type fun(id:number):string
J.I2C = function(id)
    if (id == nil or type(id) ~= "number") then
        stack(id)
        return
    end
    local ic = cc.i[id]
    if (ic == nil) then
        ic = ('>I4'):pack(id)
        cc.i[id] = ic
        cc.c[ic] = id
    end
    return ic
end

--- 中文转码
---@type fun():string
J.N2C = function(...)
    local ns = { ... }
    if (#ns == 0) then
        return ""
    end
    local s = ""
    for _, n in ipairs(ns) do
        must(FRAMEWORK_N2C[n] ~= nil)
        s = s .. FRAMEWORK_N2C[n]
    end
    return s
end

--- 保证
---@type fun(execute:function,fail:function,done:function,...:any):any
J.Promise = function(execute, fail, done, ...)
    if (type(execute) == "function") then
        local r, i = xpcall(execute, debug.traceback, ...)
        if (r == true) then
            if (type(done) == "function") then
                done()
            end
            return i
        else
            print(i)
            if (type(fail) == "function") then
                fail()
            end
        end
    end
end